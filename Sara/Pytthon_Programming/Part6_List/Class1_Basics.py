###### LIST
#**************** Slicing *********************

print('-'*15,'List Slicing','-'*15)
l1=[4,7,12,50,70,33,21]

print(l1[0:4:1]) # [4, 7, 12, 50]
print(l1[3::]) # [50, 70, 33, 21]
print(l1[0::2]) # [4, 12, 70, 21]
print(l1[-2:-6:-1]) # [33, 70, 50, 12]
print(l1[-1::-1]) # [21, 33, 70, 50, 12, 7, 4]
