#for loops practice
#range(start, end value, step)

#print values from 1 to 10
for i in range(1,10,1):
    print(i)

print("_"*40)

#step 2
for t in range (1,5,2):
    print(t)

print("_" * 40)

#print in reverse order

for a in range(10,4,-2):
    print(a)

print("_" * 40)

for b in range(-5, 1, 1):
    print(b)

print("_" * 40)

#default start value 0 and step value1
for c in range(-3):
    print(c)

print("_" * 40)

#apply loop on different data types
list1 = [1,3,5,7,9]
for d in list1:
    print(d)
print("_" * 40)

#apply loop on string
e = "sumeet"
for f in e:
    print(f)

print("_" * 40)

